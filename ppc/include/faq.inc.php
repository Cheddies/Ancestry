		<dt>What certificates can I order ?</dt>
		<dd><p>We supply full (long form) official certified birth, marriage and death certificates registered in England and Wales.</p></dd>
		
		<dt>What is a GRO index reference, and where can I find it?</dt>
		<dd><p>The General Register Office allocates a reference to every event of birth, marriage or death registered in England and Wales. This relates to the year, quarter, and district that the entry was registered in. By giving the office this index reference it allows them to quickly identify the correct entry. This reference is only of use to the General Register Office and not to local registration offices who have their own system.
		</p></dd>
		
		<dt>What information will I see on a certificate?</dt>
		<dd><p>The details contained on a full birth certificate include:</p>
		<ul>
			<li>Name, date and place of birth.</li>
			<li>Father's name (if given at time of registration), place of birth and occupation.</li>
			<li>Mother's name, place of birth, maiden surname and, after 1984, occupation.</li>
			<li>(Registrations made before 1969 do not include details of the parents' place of birth and mother's occupation.)</li>
		</ul>
		<p>The details contained on a marriage certificate include:</p>
		<ul>
			<li>Date and place of marriage.</li>
			<li>Name, age and marital status of man and woman.</li>
			<li>Occupation and usual address.</li>
			<li>Name and occupation of each party's father.</li>
			<li>Names of the witnesses.</li>
			<li>Name of the person who solemnised the marriage.</li>
		</ul>
		<p>The details contained on a death certificate include:</p>
		<ul>
			<li>Name, date and place of death.</li>
			<li>Date and place of birth (before 1969 a certificate only showed age of deceased).</li>
			<li>Occupation and usual address.</li>
			<li>Cause of death.</li>
			<li>The person who gave information for the death registration.</li>
		</ul>
		</dd>
		
		<dt>The certificate is not mine. Can I still order it?</dt>
		<dd><p>Under UK legislation, birth, marriage and death certificates are designated as 'public records', and as such anyone can request a duplicate certificate to be produced. The only caveat to this is that for births that occurred within the past 50 years, the full details are required to be provided (which includes full date of birth, and parents' names including the mother's maiden name). This is to protect against identity fraud.</p>
		</dd>
		
